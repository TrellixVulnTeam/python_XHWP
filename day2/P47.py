lista = [-3, 10, 10]

if lista[0] > 0 and lista[1] > 0:
    print("Pierwszy i drugi element listy są większe od zera.")
elif lista[0] > 0:
    print("Pierwszy element listy jest większy od zera.")
elif lista[1] > 0:
    print("Drugi element listy jest większy od zera.")
else:
    print("Pierwszy i drugi element listy są mniejsze lub równe zero.")
